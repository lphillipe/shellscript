#!/usr/bin/env bash



#-------------------------VARIÁVEIS------------------------------------------
USUARIOS="$(cat /etc/passwd | cut -d : -f 1)"
MENSAGEM_USO="
  $(basename $0) - [OPÇÕES]

  -h - Menu de Ajuda
  -v - Versão
  -s - Ordernar a saída
"
VERSAO="v1.0"
#-----------------------------------------------------------------------------

#-------------------------TESTE------------------------------------------------

#------------------------------------------------------------------------------

#-------------------------FUNÇÕES----------------------------------------------

#------------------------------------------------------------------------------

#-------------------------EXECUÇÃO---------------------------------------------
case "$1" in
  -h) echo "$MENSAGEM_USO" && exit 0 ;;
  -v) echo "$VERSAO" && exit 0       ;;
  -s) echo "$USUARIOS" && exit 0     ;;
   *) echo "$USUARIOS"               ;;
 esac
